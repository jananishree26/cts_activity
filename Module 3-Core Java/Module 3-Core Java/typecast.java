public class typecast {
    public static void main(String[] args) {
        double num1 = 45.89;
        int res1 = (int) num1;
        System.out.println("Double value: " + num1);
        System.out.println("int value: " + res1);
        int num2 = 25;
        double res2 = (double) num2;
        System.out.println("Integer value: " + num2);
        System.out.println("double value: " + res2);
    }
}